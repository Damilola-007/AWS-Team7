function readmoreA(id)
{
  document.getElementById('show-cutA_'+id).style.display='none';
  document.getElementById('read-moreA_'+id).style.display='none';
  document.getElementById('show-contentA_'+id).style.display='block';
  document.getElementById('show-lessA_'+id).style.display='block';
}

function showlessA(id)
{
  document.getElementById('show-cutA_'+id).style.display='block';
  document.getElementById('read-moreA_'+id).style.display='block';
  document.getElementById('show-contentA_'+id).style.display='none';
  document.getElementById('show-lessA_'+id).style.display='none';
}

